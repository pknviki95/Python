#!/usr/bin/python3.9

# bin() - To convert other base form to binary form

#decimal to binary:

dec_bin=53
print("The binary form of dec_bin : ",bin(dec_bin))

# octal to binary:

oct_bin=0o123
print("The binary form of oct_bin : ",bin(oct_bin))

# hexadecimal to binary:

hex_bin=0xface
print("The binary form of hex_bin : ",bin(hex_bin))
